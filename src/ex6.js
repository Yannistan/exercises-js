function add(n1, n2) {
    return n1 + n2;
}

function sub(n1, n2) {
    return n1 - n2;
}

function mul(n1, n2) {
    return n1 * n2;
}

function div(n1, n2) {
    return n1 / n2;
}

let nb1 = add(10, 11);
console.log(nb1);
let nb2 = sub(6, 4);
console.log(nb2);
let nb3 = mul(4, 5);
console.log(nb3);
let nb4 = div(10, 2);
console.log(nb4);